
//funcion que retorna texto en mayusculas
function mayusculas(text){
    return text.toUpperCase();
}

//funcion que retorne texto en minisculas
function minusculas(text){
    return text.toLowerCase();
} 

//funcion que reciba 2 numeros y los reste
function resta(a,b){
    return a - b;
}

//funcion que reciba 2 numeros y los divida
function dividir(a,b){
    return a / b;
}

//funcion que reciba 2 numeros y los multiplique
function multiplicar(a,b){
    return a * b;
}

//funcion que reciba string y devuelva longitud de este
function lenText(text){
    return text.length;
}


